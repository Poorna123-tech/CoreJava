package revise;

public class SwitchEx {

	public static void main(String[] args) {
         int a = 5;
         switch(a){
         case 10:
        	 System.out.println(a);
         case 20:
        	 System.out.println(a);
         case 30:
        	 System.out.println(a);
         
         default:
        	 System.out.println("default");
        
         }
         	 
	}

}
